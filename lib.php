function block_partnerships_pluginfile($course, $birecord_or_cm, $context, $filearea, $args, $forcedownload, array $options=array()) {
    global $DB, $CFG, $USER;

    /*$itemid = (int)array_shift($args);

    if ($itemid != 0) {
        return false;
    }*/

    $fs = get_file_storage();

    $filename = array_pop($args);
    $filepath = $args ? '/'.implode('/', $args).'/' : '/';

    error_log ("lib.php filename: $filename");
    error_log ("lib.php filepath: $filepath");
    error_log ("lib.php context->id: $context->id");

    if (!$file = $fs->get_file($context->id, 'block_partnerships', 'content', 0, $filepath, $filename) or $file->is_directory()) {
        send_file_not_found();
    }

    // NOTE: it woudl be nice to have file revisions here, for now rely on standard file lifetime,
    //       do not lower it because the files are dispalyed very often.
    \core\session\manager::write_close();
    send_stored_file($file, null, 0, $forcedownload, $options);
}
